    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);

        PedidosDao dao = new PedidosDao();
        dao.searchByStatusTrue().forEach(d1 -> {
            modelo.addRow(new Object[]{
                d1.getProduto(),
                d1.getQuantidade(),
                d1.getPrecoPago()
            });
        });
    }
  
    
  
