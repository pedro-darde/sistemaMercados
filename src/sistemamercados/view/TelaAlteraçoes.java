    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     Produtos prod = new Produtos();
     ProdutoDao dao = new ProdutoDao();
     dao.search(jLabel7.getText()).forEach(d1->{
     prod.setDescricao(jLabel2.getText());
     prod.setPreco(d1.getPreco());
     prod.setQuantidade(Integer.parseInt(tf_quantidadeAtual.getText()));
     prod.setImagemProduto(d1.getImagemProduto());
     prod.setFornecedor(d1.getFornecedor());
     prod.setPrecoVenda(Double.parseDouble(tf_novoPreco.getText()));
     prod.setPercentualLucro(d1.getPercentualLucro());
     prod.setCodProd(Integer.parseInt(jLabel7.getText()));
     dao.update(prod);
     dispose();
     });
    
    }//GEN-LAST:event_jButton1ActionPerformed

