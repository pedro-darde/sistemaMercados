    public TelaAlteraçoes(String busca) {
        initComponents();
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                ProdutoDao dao = new ProdutoDao();
        dao.search(busca).forEach(d1 ->{
            tf_precoatual.setText(String.valueOf(d1.getPrecoVenda()));
            tf_quantidadeAtual.setText(String.valueOf(d1.getQuantidade()));
            jLabel2.setText(d1.getDescricao());
            jLabel7.setText(String.valueOf(d1.getCodProd()));
        });
       tf_precoatual.setEnabled(false);
       tf_quantidadeAtual.setEnabled(false);
       this.setTitle(" Tela Alterações de Estoque");
        
    }
