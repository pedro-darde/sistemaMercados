    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        UsuarioDao dao = new UsuarioDao();
  
         Admin admin = new Admin();  
       
        dao.read().forEach(d1->{
            if(d1.getSenha().equals(tf_password.getText())  && d1.getUser().equals(tf_usuario.getText())&& (d1.isIsAdmin() == true)){
               TelaPrincipal tela = new TelaPrincipal(tf_usuario.getText());
                tela.setVisible(true);
                dispose();
                
            }else if(d1.getSenha().equals(tf_password.getText())  && d1.getUser().equals(tf_usuario.getText())&& (d1.isIsAdmin() == false)){
                
                TelaUserNormal tela1 = new TelaUserNormal(tf_usuario.getText());
                tela1.setVisible(true);
                  dispose();
            }else 
                if(admin.getSenha().equals(tf_password.getText()) && admin.getUsuario().equals(tf_usuario.getText())){
                TelaPrincipal tela2 = new TelaPrincipal(tf_usuario.getText());
                tela2.setVisible(true);
                  dispose();
            }
            
        
            
          
        });

    }//GEN-LAST:event_jButton1ActionPerformed

