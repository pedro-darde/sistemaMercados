    public void readJTable() {
        PedidosDao dao = new PedidosDao();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);

        dao.searchByIdPedidoDiferente().forEach(d1 -> {
            modelo.addRow(new Object[]{
                d1.getNumeroPedido(),
                d1.getPrecoTotal(),
                d1.getQuantidadeTotal(),
                d1.getPesoPedido(),
                d1.getStatus()
            });
        });

    }

